      <h5>Prevention tips</h5>
      <p>Zoonotic diseases are common everywhere in the world. However, the United States and other countries work constantly to reduce the number of illnesses caused by animals and insects. One way they do this is through creating food safety regulations. These regulations reduce the chances of getting a zoonotic disease from something you eat in a developed country.<br>

There are also ways to help prevent getting a zoonotic disease. These include the following:<br>

 -> Wash your hands diligently.<br>
 -> Use insect repellent or other methods to keep mosquitos, fleas, and ticks away.<br>
 -> Practice safe food handling. This includes washing off all produce before eating it.<br>
 -> Avoid being bitten or scratched by an animal.<br>
 -> Have your pets vaccinated and take them for regular annual visits to the veterinarian.<br>
 -> Talk to your veterinarian about appropriate flea and tick preventatives for your pets.<br>
 -> Check for ticks when you’ve been outside.<br>
 -> Don’t eat, drink, or touch your eyes or mouth while you’re handling or in close contact with animals.<br>
 -> Use gloves if you need to handle an animal that is or appears to be sick.<br>
 -> Keep any areas where animals are kept clean and sanitary.<br>
 -> Be aware of areas where animals or insects might be when you’re out in nature, especially when you participate in activities like hunting and camping.<br>
 -> Don’t handle or approach any animal in the wild that appears sick. Be sure to contact animal control or the local government to have the sick animal removed.
