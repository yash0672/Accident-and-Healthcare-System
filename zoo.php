      <h1 class="w3-text-black"><u>Zoonosis</u></h1>
      <p>A zoonosis is an infectious disease that has jumped from a non-human animal to humans. Zoonotic pathogens may be bacterial, viral or parasitic, or may involve unconventional agents and can spread to humans through direct contact or through food, water or the environment. They represent a major public health problem around the world due to our close relationship with animals in agriculture, as companions and in the natural environment. Zoonoses can also cause disruptions in the production and trade of animal products for food and other uses.<br>

Zoonoses comprise a large percentage of all newly identified infectious diseases as well as many existing ones. Some diseases, such as HIV, begin as a zoonosis but later mutate into human-only strains. Other zoonoses can cause recurring disease outbreaks, such as Ebola virus disease and salmonellosis. Still others, such as the novel coronavirus that causes COVID-19, have the potential to cause global pandemics. </p><br>
      
      

        <h4>Who is at Risk?</h4>
        <p>Zoonotic pathogens can spread to humans through any contact point with domestic, agricultural or wild animals. Markets selling the meat or by-products of wild animals are particularly high risk due to the large number of new or undocumented pathogens known to exist in some wild animal populations. Agricultural workers in areas with a high use of antibiotics for farm animals may be at increased risk of pathogens resistant to current antimicrobial drugs. People living adjacent to wilderness areas or in semi-urban areas with higher numbers of wild animals are at risk of disease from animals such as rats, foxes or raccoons. Urbanization and the destruction of natural habitats increase the risk of zoonotic diseases by increasing contact between humans and wild animals.

