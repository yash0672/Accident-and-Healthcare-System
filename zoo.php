      <h4>How they are transmitted?</h5>
      <p>Zoonoses can be transmitted in various ways:<br>

 -> through the air<br>
 -> by eating contaminated meat or produce<br>
 -> through close contact with an infected animal<br>
 -> by touching an area or surface that an infected animal touched<br>
 -> through insect bites like mosquitos or ticks<br>
Many transmissions occur when people hike, bike, boat, or enjoy other activities in the great outdoors.<br>

Petting zoos are also common places for a zoonotic disease to be transmitted.

Those who live and work on farms are in close contact with many types of livestock. Livestock is a common carrier of many zoonoses.<br>

Your family pet can carry ticks and fleas indoors that can then move onto you and your family.

</p> <br>

      <h4>What to do if you have a zoonotic disease?</h4>
      <p>If you have or think you have a zoonotic disease, you should contact a medical professional as soon as possible.

If you’re scratched or bitten by an animal, be sure to have the animal thoroughly checked by a veterinarian. This is to make sure that they’re appropriately vaccinated and don’t have rabies or other zoonotic diseases.<br>

If you’ve been bitten by a tick, try to preserve the tick after removal in a safe container. This way it can be identified to narrow down the possible diseases that it might transmit and tested for any of those diseases.<br>

Although zoonotic diseases are common, some people are at higher risk of getting them. These individuals may also have more severe reactions and symptoms. If you’re one of these individuals at risk, you should seek medical attention immediately if you think you may have a zoonotic disease. High-risk individuals include:<br>

 -> pregnant women<br>
 -> adults aged 65 or older<br>
 -> children 5 years old or younger<br>
 -> those with HIV<br>
 -> those with cancer who are going through chemotherapy<br>
 -> others with weakened immune systems


