      <h3>If I don't feel injured after my accident, do I have to see a Doctor?</h3>
        <p>Both you and your passengers should always consider seeing a doctor for an evaluation after an auto accident. The doctor might recognize underlying injuries, sometimes serious injuries, that are not apparent to you.</p><br><br>
    
  <h3>How soon after an Accident should I access and document my injuries?</h3>
  <p>Call Accident Helpline at 1073\1033 as soon as possible. Our case consultations are always free of charge. When you are injured in an auto accident, insurance companies investigate immediately. The longer you wait to access and document your injuries, the quicker insurance companies can deny you the proper medical care you are entitled to.</p><br><br>
  
<h3>Can I go to any Doctor or the Auto insurance company gets to decide?</h3>
  <p>In auto accident cases, your personal injury protection (PIP) insurance company has no right to choose your health care provider or doctor. You can go to any doctor you want. With other kinds of insurance — such as health insurance — you might have to choose a doctor who is on your list of preferred providers or primary care physicians depending on the type of health insurance you have. Accident Helpline can review this with you and help you make a sound determination.</p><br><br>


<h3>What should I do when the insurance company asks about my injuries?</h3>
  <p>Immediately contact Accident Helpline at 1073\1033 to advise you and protect your medical rights. Often, the insurance company behind the at-fault party will contact you to take a statement that they will use against you later. The insurance company will look to gather information to prevent you from obtaining the treatment you are entitled to.</p><br><br>

  <h3>How soon after an Auto Accident should I consider getting a Lawyer?</h3>
  <p>Quickly. To be safe, you should talk to an attorney as soon as possible, especially if the injuries are severe or a death has resulted. All too often, valuable evidence disappears, witnesses move, memories grow dim and the practical ability to prove your case might diminish. When you are being treated by Accident Helpline, an attorney can also provide you with guidance concerning unpaid bills and getting needed treatment. Legal advice can also be useful if you have questions about the settlement value of a claim, your insurance policy’s terms, or suspect bad faith on the part of your insurance company.</p><br><br>

  <h3>What if it turns out my injuries are worse than I expected many years from now?</h3>
  <p>Usually, personal injury settlements are final, at which time you will be required to sign a full general “release” of all claims. The same is true of a jury awarding you money.  There is, however, a number of exceptions to this. You should consult with an experienced auto accident law firm for your particular situation.</p><br><br>

  <h3>Do you accept my Insurance?</h3>
  <p>We do not accept all in-state and out of state car insurances. However we can arrange the insurance setup on a common basis.</p><br><br>

  <h3>Will the other person's insurance company pay my medical bills?</h3>
  <p>Yes, the at-fault party’s insurance company is responsible to pay for your medical treatment. If the at-fault was not insured, an attorney will be needed to file a suit against the at-fault party to recover damages including but not limited too medical bills.</p><br><br>

  <h3>Will the other person's insurance company pay for my lost wages?</h3>
  <p>“Out of pocket” wage loss and lost earning ability should also be factored into a final value evaluation and settlement of your case.</p><br><br>

  <h3>Will the other person's insurance company pay me an "advance" for my injuries and then reach a final settlement later?</h3>
  <p>No, they will not normally do this. If the “at-fault” party’s insurance company pays at all, it will usually be a lump sum at the end of the case and it is final, often referred to as a “settlement”.</p><br><br>

