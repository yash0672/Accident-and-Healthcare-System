<center><h1>General Covid-19 Vaccine FAQ's</h1></center><br><br>
<button class="accordion"> Which COVID-19 Vaccines are licensed in India?</button>
<div class="panel">
  <p>Two vaccines that have been granted emergency use authorization by the Central Drugs Standard Control Organization (CDSCO) in India are Covishield® (AstraZeneca's vaccine manufactured by Serum Institute of India) and Covaxin® (manufactured by Bharat Biotech Limited).</p>
</div>
<hr>
<button class="accordion"> What is Emergency Use Authorization(EUA)/Permission for restricted ues?</button>
<div class="panel">
  <p>Emergency Use Authorization (EUA) is a regulatory mechanism to allow the use of vaccines and medicines to prevent and/or reduce the impact of life-threatening diseases or conditions as caused by COVID-19. However, before grant of the EUA, rigorous assessments of laboratory and clinical trial data, including data on quality, safety, production of protective antibodies and efficacy is conducted. Safety is particularly critical aspect of this scrutiny and a risk-versus- benefit evaluation is done in the context of a public health emergency. Full licensure is obtained when the manufacturer submits the complete data. EUA by Indian regulators is aligned with global guidelines.</p>
</div>
<hr>
<button class="accordion"> Is the EUA a new process introduced for COVID-19 Vaccines?</button>
<div class="panel">
  <p>Concept of EUA always existed to save the lives of people all over the world with vaccine and medicines for life threatening diseases while companies continue to obtain additional safety and effectiveness information to enable full licensure. Previously, EUAs have been granted to vaccines for outbreaks due to Anthrax, Ebola, Enterovirus, H7N9 Influenza, and Middle East Respiratory Syndrome. As of January 2021, nine COVID-19 vaccines were in emergency use in numerous countries around the globe.</p>
</div>
<hr>
<button class="accordion"> Have the Vaccines undergone the needed trials before EUA?</button>
<div class="panel">
  <p>Both the Indian COVID-19 vaccines have completed their Phase I & II trials. Covishield® has completed its Phase III trials in UK and the bridging trial in India.</p>
</div>
<hr>
<button class="accordion"> What is phase I,II & III for the clinical trial of the Vaccine?</button>
<div class="panel">
  <p>Vaccine trial phases includes:-

Pre-clinical: Vaccine development in laboratory animals
Phase 1 : Clinical trial (small number of participants): Assess vaccine safety, immune response and determine right dosage (short duration)<br>
Phase 2 : Clinical trial (few hundred participants): Assess safety and the ability of the vaccine to generate an immune response (short duration)<br>
Phase 3 : Clinical trial (thousands of participants): Determine vaccine effectiveness against the disease and safety in a larger group of people (duration 1-2 years)</p>
</div>
<hr>
<button class="accordion"> Why is Vaccine not provided to children who are a usual target?</button>
<div class="panel">
  <p>COVID-19 affects all age groups; however, morbidity & mortality is several times higher in adults particularly in those above the age of 50 years. Children have either asymptomatic or mild infection. The general practice is to first evaluate any new vaccine in older population and then age reduction is done to assess the safety and effectiveness in paediatric population. The currently available vaccines have not been evaluated in children so far. There are some clinical trials now underway to test the effectiveness and safety of the COVID-19 vaccines in children.</p>
</div>
<hr>
<button class="accordion"> What technology has been used in development of two available vaccines in India?</button>
<div class="panel">
  <p>Covishield® vaccine, manufactured by the Serum Institute of India, is a Viral Vector-based Technology which is also used to manufacture Ebola vaccine.

Covaxin® vaccine, manufactured by the Bharat Biotech, is a Whole-virion Inactivated Coronavirus Vaccine which is also used to manufacture vaccines like Influenza, Rabies and Hepatitis- A.</p>
</div>
<hr>
<button class="accordion"> What is the composition of both the Vaccines?</button>
<div class="panel">
  <p>Composition of Covishield includes inactivated adenovirus with segments of Coronavirus, Aluminium Hydroxide Gel, L-Histidine, L-Histidine Hydrochloride Monohydrate, Magnesium Chloride Hexahydrate, Polysorbate 80, Ethanol, Sucrose, Sodium Chloride, and Disodium Edetate Dihydrate (EDTA). Composition of Covaxin includes inactivated Coronavirus, Aluminum Hydroxide Gel, TLR 7/8 Agonist, 2-Phenoxyethanol and Phosphate Buffered Saline [NKA1].</p>
</div>
<hr>
<button class="accordion"> What is the dose schedule for both the Vaccines?</button>
<div class="panel">
  <p>The time interval between two doses of the Covishield vaccine has been extended from four-six weeks to four-eight weeks. The second dose of Covaxin can be taken four to six weeks after the first.</p>
</div>
<hr>
<button class="accordion"> Do I have a choice of Vaccine I will recieve?</button>
<div class="panel">
  <p>The time interval between two doses of the Covishield vaccine has been extended from four-six weeks to four-eight weeks. The second dose of Covaxin can be taken four to six weeks after the first.</p>
</div>
<hr>
<button class="accordion"> Is the COVID-19 Vaccine scheduled anytime soon for me?</button>
<div class="panel">
  <p>The COVID-19 vaccine was launched on 16th January, 2021. The first group includes healthcare and frontline workers. The second group to receive COVID-19 vaccine were persons over 60 years of age as of January 1st, 2021 and persons between 45 and 59 years with comorbid conditions. This group started receiving vaccinations from March 1st, 2021. From April 1st, 2021, People above the age of 45 years (born before 1st Jan, 1977) are eligible to get the COVID-19 vaccine. From May 1, 2021, all eligible citizens above the age of 18 years can get the COVID-19 vaccine.</p>
</div>
<hr>
<button class="accordion"> Is it mandatory to take the Vaccine?</button>
<div class="panel">
  <p>Vaccination for COVID-19 is voluntary. However, it is advisable to receive the complete schedule of COVID-19 vaccine for protecting oneself against this disease and also to limit the spread of this disease to the close contacts including family members, friends, relatives and co-workers.</p>
</div>
<hr>
<button class="accordion"> Among multiple vaccines, how is one or more vaccine choosen for administration?</button>
<div class="panel">
  <p>The safety and efficacy data from clinical trials of vaccine candidates are examined by Drug Regulator of our country before granting the license for the same. Hence, all the COVID-19 vaccines that receive license will have comparable safety and efficacy. However, it must be ensured that the entire schedule of vaccination is completed by only one type of vaccine as different COVID-19 vaccines are not interchangeable.</p>
</div>
<hr>
<button class="accordion"> Is the Indian Vaccine as good as Vaccines available in other Countries?</button>
<div class="panel">
  <p>Yes, the COVID-19 vaccine introduced in India will be as effective as any vaccine developed by other countries. Various phases of vaccine trials are undertaken to ensure its safety and efficacy.</p>
</div>
<hr>
<button class="accordion"> Our Scientists have developed a vaccine against the novel coronavirus in such a short time. How was this possible?</button>
<div class="panel">
  <p>Developing a vaccine generally involves years of research. First, we need a vaccine candidate that is evaluated in animals for its safety and efficacy. After a vaccine candidate passes a pre- clinical trial, it enters the clinical trial phase. While scientists have worked round the clock in the laboratory, even regulatory approvals which used to take several months have been fast tracked. It helped eliminate all the time lapses between the pre-clinical and clinical trial stages. Earlier, the vaccine development involved a series of steps, but in the case of the coronavirus vaccine, the scientists and regulators worked in tandem, accelerating the whole process without compromises on any protocols and any step.</p>
</div>
