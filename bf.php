      <h5>What happens when one contracts it?</h5>
      <p>Warning signs include pain and redness around the eyes or nose, with fever, headache, coughing, shortness of breath, bloody vomits, and altered mental status. According to the advisory, infection with mucormycetes should be suspected when there is:<br><br>

      * Sinusitis — nasal blockade or congestion, nasal discharge (blackish/bloody);<br>
      * Local pain on the cheek bone, one-sided facial pain, numbness or swelling;<br>
      * Blackish discoloration over bridge of nose/palate;<br>
      * Loosening of teeth, jaw involvement;<br>
      * Blurred or double vision with pain;<br>
      * Thrombosis, necrosis, skin lesion;<br>
      * Chest pain, pleural effusion, worsening of respiratory symptoms.<br><br>

      Experts advise that one should not count all cases of blocked nose as cases of bacterial sinusitis, particularly in the context of immunosuppression and/or Covid-19 patients on immunomodulators. Do not hesitate to seek aggressive investigations for detecting fungal infection, they advise.</p> <br>

      <h5>What’s the treatment?</h5>
      <p>While it is treated with antifungals, mucormycosis may eventually require surgery. Doctors have said that it is of utmost importance to control diabetes, reduce steroid use, and discontinue immunomodulating drugs. To maintain adequate systemic hydration, the treatment includes infusion of normal saline (IV) before infusion of amphotericin B and antifungal therapy, for at least 4-6 weeks.<br><br>

      Experts in the task force have stressed the need to control hyperglycemia , and monitor blood glucose level after discharge following Covid-19 treatment, and also in diabetics. One should use steroids judiciously — correct timing, correct dose and duration are important.<br><br>

      Management of Covid patients with mucormycosis is a team effort involving microbiologists, internal medicine specialists, intensivist neurologist, ENT specialists, ophthalmologists, dentists, surgeons (maxillofacial/plastic) and others.

