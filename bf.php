      <h5>How can one prevent it?</h5>
      <p>One should remember that it is a rare disease. However, some groups of people are more vulnerable than others. What predisposes patients is uncontrolled diabetes mellitus, immunosuppression by steroids, prolonged ICU stay, and comorbidities — post transplant/malignancy, voriconazole therapy.<br><br>

      Experts advise that you use masks if you are visiting dusty construction sites. Wear shoes, long trousers, long-sleeved shirts and gloves while handling soil (gardening), moss or manure. Maintain personal hygiene including a thorough scrub bath.<br><br></p>
