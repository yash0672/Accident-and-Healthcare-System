      <h5>What is mucormycosis?</h5>
      <p>Mucormycosis, commonly called black fungus, is a rare but serious fungal infection caused by a kind of fungus called mucormycete, which is abundant in the environment. It mainly affects people who have health problems or take medicines that lower the body’s ability to fight germs and sickness.</p>
      <h5>Symptoms</h5>
      <p>These include pain and redness around eyes and/or nose, fever, headache, coughing, shortness of breath, bloody vomits, and altered mental status. Warning signs can include toothache, loosening of teeth, blurred or double vision with pain.</p>
      <h5>Who is vulnerable</h5>
      <p>Vulnerable groups include people who have health problems or take medicines that lower the body’s ability to fight germs and sickness. These include those with diabetes, cancer, or those who have had an organ transplant.</p>
      <h5>Prevention</h5>
      <p>Use masks if you are visiting dusty construction sites. Wear shoes, long trousers, long-sleeved shirts and gloves while gardening. Maintain personal hygiene including a thorough scrub bath.</p>
      <h5>Diagnosis</h5>
      <p>It depends on the location of the suspected infection. A sample of fluid from your respiratory system may be collected for testing in the lab; otherwise a tissue biopsy or a CT scan of your lungs, sinuses etc may be conducted.</p>
      <h5>Treatment</h5>
      <p>Mucormycosis needs to be treated with prescription antifungal medicine. In some cases, it can require surgery; it can lead to eventual loss of the upper jaw and sometimes even an eye.</p>

